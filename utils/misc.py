def convert_items_in_list_to_lower(list_to_convert):
    new_list = []
    for item in list_to_convert:
        new_list.append(item.lower())
        
    return new_list
    
def convert_items_in_list_to_capitalized(list_to_convert):
    new_list = []
    for item in list_to_convert:
        new_list.append(item.capitalize())
        
    return new_list